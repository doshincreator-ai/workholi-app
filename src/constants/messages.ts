export const MILESTONES = Object.keys(MILESTONE_MESSAGES).map(Number).sort((a, b) => a - b);

export function getRandomEncouragement(): string {
  return ENCOURAGEMENT_MESSAGES[Math.floor(Math.random() * ENCOURAGEMENT_MESSAGES.length)];
}

/** 節目を超えた最大のものを返す */
export function getCrossedMilestone(prevTotal: number, newTotal: number): number | null {
  for (const m of [...MILESTONES].reverse()) {
    if (prevTotal < m && newTotal >= m) return m;
  }
  return null;
}
